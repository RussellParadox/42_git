#include <stddef.h>

void	*ft_memset(void *s, int c, size_t n)
{
	int	*s_toint;

	s_toint = (int *)s;
	while (n > 0)
	{
		*s_toint++ = c;
		n -= sizeof(n);
	}
	return (s);
}
